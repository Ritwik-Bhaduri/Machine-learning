x1=rnorm(30,0,2)
y1=rnorm(30,50,2)
x2=rnorm(30,20,2)
y2=rnorm(30,20,2)
x3=rnorm(30,40,2)
y3=rnorm(30,10,2)
x4=rnorm(30,60,2)
y4=rnorm(30,20,2)
x=c(x1,x2,x3,x4)
y=c(y1,y2,y3,y4)
z=cbind(x,y)
plot(z,pch=16,cex=0.5)
r=kmeans(z,4,iter.max=20)
plot(z,col=r$cluster,pch=16)
points(r$centers,pch=8)
